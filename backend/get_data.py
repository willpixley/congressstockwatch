def get_house_committees():
    for committee in house_committees.keys():
        members = house_committees[committee]
        c_sector = Sector.objects.get(
            sector_code=gics_sectors[house_committees_sectors[committee]]
        )
        c = Committee(committee_name=committee, chamber="H", sector=c_sector)
        c.save()
        for member in members:
            # Last name
            lname = member.split(",")[0]
            name = member.split(",")
            fname = name[1].split("(")[0].strip()
            # Two letter state code
            state = member.split("(")[-1][:-1]
            try:
                m = CongressMember.objects.get(last_name=lname, chamber="H")
                c.committee_members.add(m)
            except:
                m = CongressMember.objects.get(
                    last_name=lname, state=state, chamber="H", first_name=fname
                )
                c.committee_members.add(m)

            finally:
                continue

        c.save()


