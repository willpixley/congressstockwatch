## Adds all members from 119 congress
def getMembers():
    url = "https://api.congress.gov/v3/member/congress/119"
    members_of_congress = []
    for i in range(0, 4):
        params = {
            "api_key": API_KEY,
            "limit": 250,
            "current_member": "true",
            "offset": 250 * i,
        }
        response = requests.get(url, params=params)
        members = response.json()["members"]
        for member in members:
            name = member["name"]
            parts = name.split(", ")
            last_name = parts[0]
            first_name = parts[1]
            middle_initial = ""

            m = {
                "bio_guide_id": member["bioguideId"],
                "party": member["partyName"][0],
                "state": us_states_territories[member["state"]],
                "chamber": member["terms"]["item"][-1]["chamber"][0],
                "first_name": first_name,
                "middle_initial": middle_initial,
                "last_name": last_name,
                "term": len(member["terms"]["item"]),
            }
            members_of_congress.append(
                CongressMember(
                    bio_guide_id=m["bio_guide_id"],
                    first_name=m["first_name"],
                    middle_initial=m["middle_initial"],
                    last_name=m["last_name"],
                    chamber=m["chamber"],
                    party=m["party"],
                    state=m["state"],
                    term=m["term"],
                )
            )
    CongressMember.objects.bulk_create(members_of_congress)
    print(f"{len(members_of_congress)} members added")



## Adds all stocks
def getStocks():
    df = pd.read_csv("./data/NASDAQ.csv")
    df["sector_code"] = df["Sector"].map(gics_sectors)
    unmatched_sectors = set()
    stocks = []
    for _, row in df.iterrows():
        sector_code = row["sector_code"]
        try:
            sector_instance = Sector.objects.get(sector_code=row["sector_code"])
        except:
            unmatched_sectors.add(row["Sector"])

        stock = Stock(sector=sector_instance, name=row["Name"], ticker=row["Symbol"])
        stocks.append(stock)
    Stock.objects.bulk_create(stocks, ignore_conflicts=True)
    print("Inserted stocks")

    print(unmatched_sectors)


